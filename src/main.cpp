Adafruit_NeoPixel strip = Adafruit_NeoPixel(1, 6, NEO_GRB + NEO_KHZ800);

void setup() {
  Serial.begin(115200);
  strip.begin();
  pinMode(A1, INPUT);

  // Listen for MIDI messages on channel 1
  MIDI.begin(1);

  MIDI.setHandleSystemExclusive(OnMidiSysEx);

}

void loop() {
  // Listen to incoming notes
  MIDI.read();
  uint8_t potRead = analogRead(A0) >> 3; //Bit shift to convert 10 bit to 7 bit
  if (potRead != oldReading) { //Only send on change of value
    oldReading = potRead; //Set oldreading to new value if changed
    MIDI.sendControlChange(1, oldReading, 2);
    strip.setPixelColor(0, 0xff0000 >> (oldReading >> 3));
    strip.show();
  }

}


void OnMidiSysEx(byte* data, unsigned length) {
  Serial.print(F("SYSEX: ("));
  Serial.print(length);
  Serial.print(F(" bytes) "));
  for (uint16_t i = 0; i < length; i++) {
    Serial.print(data[i], HEX);
    Serial.print(" ");
  }
  Serial.println();
}